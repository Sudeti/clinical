# 5. Update requirements.txt to include psycopg2-binary
if ! grep -q "psycopg2-binary" requirements.txt; then
    echo "psycopg2-binary>=2.9.0" >> requirements.txt
fi

# 6. Rename project references (if any exist in files)
find . -type f \( -name "*.py" -o -name "*.html" -o -name "*.md" -o -name "*.txt" \) ! -path "./venv/*" ! -path "./.git/*" | xargs sed -i '' "s/$TEMPLATE_NAME/$PROJECT_NAME/g" 2>/dev/null || true

# 7. Set up virtual environment
python3 -m venv venv
source venv/bin/activate

# Upgrade pip first to avoid installation issues
pip install --upgrade pip

# Install requirements
echo "Installing Python packages..."
pip install -r requirements.txt

# Verify psycopg2-binary is installed
if ! python -c "import psycopg2" 2>/dev/null; then
    echo "Warning: psycopg2-binary installation may have failed. Attempting to install directly..."
    pip install psycopg2-binary
fi

# 8. Set up git repository
rm -rf .git
git init
cat > .gitignore << EOL
