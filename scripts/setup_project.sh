#!/bin/bash

# Check if project name is provided
if [ $# -eq 0 ]; then
    echo "Usage: ./scripts/setup_project.sh <project_name>"
    exit 1
fi

PROJECT_NAME=$1
TEMPLATE_NAME="sovereigncritic"
CURRENT_DIR=$(pwd)

echo "Setting up new Django project: $PROJECT_NAME"

# 1. Create project directory
mkdir -p ../$PROJECT_NAME
cp -r ./* ../$PROJECT_NAME/
cd ../$PROJECT_NAME

# Remove the original db.sqlite3 file
rm -f db.sqlite3

# 2. Generate new secret keys
SECRET_KEY=$(python3 -c "import secrets; print(secrets.token_urlsafe(50))")

# 3. Create new .env file
cat > .env << EOL
# Security
SECRET_KEY=$SECRET_KEY
DEBUG=True

# Database
DB_NAME=$PROJECT_NAME
DB_USER=postgres
DB_PASSWORD=postgres
DB_HOST=localhost
DB_PORT=5432

# Allowed Hosts (comma-separated)
ALLOWED_HOSTS=localhost,127.0.0.1

# API Keys (update these with your own keys)
ANTHROPIC_API_KEY=your_anthropic_api_key_here
EOL

# 4. Update settings.py to use PostgreSQL
cat > config/settings.py << 'EOL'
"""
