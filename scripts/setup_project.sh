echo ""
echo "Project setup complete! Your new Django project '$PROJECT_NAME' is ready."
echo ""
echo "Next steps:"
echo "1. CD into your project: cd ../$PROJECT_NAME"
echo "2. Activate virtual environment: source venv/bin/activate"
echo "3. Create PostgreSQL database: createdb $PROJECT_NAME"
echo "4. Update .env file with your API keys if needed"
